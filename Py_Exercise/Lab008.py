# Dynamically typed
# Dynamic Type - Type of Data that Python supports.
age = 70
# variable name = variable value
# identifier = literal

# Types which are supported in Python are
# Integers - Positive and Negative whole numbers.
# 1, 6, 9, -5, 15, -57

# Floating Positive numbers
# 3.14, 5.98789, 18.0000, -4.58585, -98.756
pi = 3.14

# String
# "Gururaj", "Alpha", "Beta", "Gama", "A", "B", "C"
name = "Gururaj"

# Boolean
# True, False
isMarried = True

# How do i check the type of the variable?
print(type(age))
print(type(pi))
print(type(name))
print(type(isMarried))

# Python - Complex number - i iota
complex_number = 2 + 3j
# Real - 2
# Imaginary - 3
print(complex_number.real)
print(complex_number.imag)

